function Cp = SpecHeat(varargin) % Specific heat of 7 species, if species fractions are provided it returns weighted specific heat of the flow
%Returns specific heat in kJ/kmol*K
Ru = 8.314;
T = varargin{1};
X =[];
if length(varargin)>1
    X = varargin{2};
end
n = length(T);
Cp = zeros(n,7);
CH4a = [2.1737833; 8.9936592e-3; -2.7855467e-6; 3.9775117e-10; -1.9976425e-14; -1.0216566e4];
CH4b = [2.9428148; 2.5153728e-3; 7.9085839e-6; -4.7495483e-9; 1.4244910e-13; -1.0056824e4;];

COa = [3.025178; 1.4426885e-3; -5.630827e-7; 1.0185813e-10; -6.91095e-15; -1.426835e4;];
COb = [3.262451; 1.5119409e-3; -3.881755e-6; 5.581944e-9; -2.474951e-12; -1.4310539e4;];

CO2a = [4.453623; 3.140168e-3; -1.2784105e-6; 2.393996e-10; -1.6690333e-14; -4.896696e4;];
CO2b = [2.275724; 9.922072e-3; -1.0409113e-5; 6.866686e-9; -2.117280e-12; -4.837314e4];

H2a = [2.991423; 7.000644e-4; -5.633828e-8; -9.231578e-12; 1.5827519e-15; -8.350340e2;];
H2b = [3.298124; 8.249441e-4; -8.143015e-7; -9.475434e-11; 4.134872e-13; -1.0125209e3;];

H2Oa = [2.672145; 3.056293e-3; -8.730260e-7; 1.2009964e-10; -6.391618e-15; -2.989921e4;];
H2Ob = [3.386842; 3.474982e-3; -6.354696e-6; 6.968581e-9; -2.506588e-12; -3.020811e4;];

N2a = [2.926640; 1.4879768e-3; -5.684760e-7; 1.0097038e-10; -6.753351e-15; -9.227977e2;];
N2b = [3.298677; 1.4082404e-3; -3.963222e-6; 5.64151e-9; -2.444854e-12; -1.0208999e3;];

O2a = [3.697578; 6.135197e-4; -1.258842e-7; 1.775281e-11; -1.1364354e-15; -1.2339301e3;];
O2b = [3.212936; 1.1274864e-3; -5.756150e-7; 1.3138773e-9; -8.768554e-13; -1.0052490e3;];

T1 = linspace(1,1,n);
T2 = T;
T3 = (T.^2); 
T4 = (T.^3); 
T5 = (T.^4); 
for i=1:1:n
    if T(i)>=1000
        Cp(i,1) = sum([T1(i)*CH4a(1); T2(i)*CH4a(2); T3(i)*CH4a(3); T4(i)*CH4a(4); T5(i)*CH4a(5);]);
        Cp(i,2) = sum([T1(i)*COa(1); T2(i)*COa(2); T3(i)*COa(3); T4(i)*COa(4); T5(i)*COa(5);]);
        Cp(i,3) = sum([T1(i)*CO2a(1); T2(i)*CO2a(2); T3(i)*CO2a(3); T4(i)*CO2a(4); T5(i)*CO2a(5);]);
        Cp(i,4) = sum([T1(i)*H2a(1); T2(i)*H2a(2); T3(i)*H2a(3); T4(i)*H2a(4); T5(i)*H2a(5);]);
        Cp(i,5) = sum([T1(i)*H2Oa(1); T2(i)*H2Oa(2); T3(i)*H2Oa(3); T4(i)*H2Oa(4); T5(i)*H2Oa(5);]);
        Cp(i,6) = sum([T1(i)*N2a(1); T2(i)*N2a(2); T3(i)*N2a(3); T4(i)*N2a(4); T5(i)*N2a(5);]);
        Cp(i,7) = sum([T1(i)*O2a(1); T2(i)*O2a(2); T3(i)*O2a(3); T4(i)*O2a(4); T5(i)*O2a(5);]);
    else
        Cp(i,1) = sum([T1(i)*CH4b(1); T2(i)*CH4b(2); T3(i)*CH4b(3); T4(i)*CH4b(4); T5(i)*CH4b(5);]);
        Cp(i,2) = sum([T1(i)*COb(1); T2(i)*COb(2); T3(i)*COb(3); T4(i)*COb(4); T5(i)*COb(5);]);
        Cp(i,3) = sum([T1(i)*CO2b(1); T2(i)*CO2b(2); T3(i)*CO2b(3); T4(i)*CO2b(4); T5(i)*CO2b(5);]);
        Cp(i,4) = sum([T1(i)*H2b(1); T2(i)*H2b(2); T3(i)*H2b(3); T4(i)*H2b(4); T5(i)*H2b(5);]);
        Cp(i,5) = sum([T1(i)*H2Ob(1); T2(i)*H2Ob(2); T3(i)*H2Ob(3); T4(i)*H2Ob(4); T5(i)*H2Ob(5);]);
        Cp(i,6) = sum([T1(i)*N2b(1); T2(i)*N2b(2); T3(i)*N2b(3); T4(i)*N2b(4); T5(i)*N2b(5);]);
        Cp(i,7) = sum([T1(i)*O2b(1); T2(i)*O2b(2); T3(i)*O2b(3); T4(i)*O2b(4); T5(i)*O2b(5);]);
    end
end
Cp = Cp*Ru;

if ~isempty(X);
    Cp = sum(Cp.*X,2);
end